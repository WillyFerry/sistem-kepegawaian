    public void loadsearch() {
        tabelbagian.getDataVector().removeAllElements();
        tabelbagian.fireTableDataChanged();
        try {
            Statement s = c.createStatement();
            sql = "select * from bagian where id_bagian like '" + tsearch.getText() + "%' || nama_bagian like '" + tsearch.getText() + "%'";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[3];
                o[0] = r.getString("id_bagian");
                o[1] = r.getString("nama_bagian");
                o[2] = r.getString("deskripsi");
                tabelbagian.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
    
    public int getLatestId(){
        int latestId = 1;
        try {
            Statement s = c.createStatement();
            sql = "select id_bagian from bagian order by id_bagian desc limit 1";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                latestId = r.getInt("id_bagian") + 1;
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println(e);
        }
        
        return latestId;
    }

    public void loadData() {
        tabelbagian.getDataVector().removeAllElements();
        tabelbagian.fireTableDataChanged();
        try {
            Statement s = c.createStatement();
            sql = "select * from bagian";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[10];
                o[0] = r.getString("id_bagian");
                o[1] = r.getString("nama_bagian");
                o[2] = r.getString("deskripsi");
                tabelbagian.addRow(o);
            }
            r.close();
            s.close();
            txt_IdBagian.setText(Integer.toString(getLatestId()));
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
        btnSimpan.setEnabled(true);
    }

