            sql = "INSERT INTO bagian VALUES('" + txt_IdBagian.getText() + "','" + txt_NamaBagian.getText() + "','" + txt_Deskripsi.getText() +"')"; 
            PreparedStatement p = c.prepareStatement(sql);
            p.execute();
